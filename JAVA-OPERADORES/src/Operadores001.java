public class Operadores001 {
    public static void main(String[] args) {
        // Operadores Aritméticos
        // Adição
        int soma = 10 + 20;
        System.out.println("Soma: " + soma); // 30

        // Subtração
        int subtracao = 10 - 5;
        System.out.println("Subtração: " + subtracao); // 5

        // Multiplicação
        int multiplicacao = 10 * 5;
        System.out.println("Multiplicação: " + multiplicacao); // 50

        // Divisão
        int divisao = 10 / 2;
        System.out.println("Divisão: " + divisao); // 5

        // Módulo (Resto da Divisão)
        int modulo = 10 % 3;
        System.out.println("Módulo: " + modulo); // 1
    }
}
